// Export a function to get content by ID
export function getContentById(id: number): Show | undefined {
  return allContent.find((item) => item.id === id)
}

// Export a function to get similar content
export function getSimilarContent(show: Show, limit = 6): Show[] {
  if (!show.genre) return []

  return allContent.filter((item) => item.id !== show.id && item.genre === show.genre).slice(0, limit)
}

// New function to get content by type
export function getContentByType(type: "movie" | "show" | "anime" | "documentary"): Show[] {
  return allContent.filter((item) => item.type === type)
}

